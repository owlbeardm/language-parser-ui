import {AfterViewChecked, ChangeDetectorRef, Component, HostListener, OnInit, ViewChild} from '@angular/core';

@Component({
  selector: 'app-header',
  templateUrl: './header.component.html',
  styleUrls: ['./header.component.css']
})
export class HeaderComponent implements OnInit, AfterViewChecked {

  @ViewChild('col') col: any;
  bcol: String = '--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------';

  constructor(private cdRef: ChangeDetectorRef) {
  }

  @HostListener('window:resize', ['$event'])
  onResize() {
    this.resize();
  }

  ngAfterViewChecked() {
    this.resize();
  }

  resize() {
    const d = '--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------';
    const coll = Math.floor((this.col?.nativeElement.offsetWidth - 2) / 8);
    const bcol = this.bcol;
    this.bcol = d.substr(0, coll);
    if (bcol != this.bcol) {
      this.cdRef.detectChanges();
    }
  }

  ngOnInit(): void {
  }

}
